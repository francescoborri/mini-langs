open Utils
open Utils.ColorText

let num_regs = ref None
let src = ref ""
let out = ref ""
let opt_check_undef_vars = ref false
let opt_liveness_optimization = ref false
let opt_constant_propagation = ref false
let opt_print_tokens = ref false
let opt_print_ast = ref false
let opt_print_mimp_cfg = ref false
let opt_print_mimp_defined_vars = ref false
let opt_print_mrisc_cfg = ref false
let opt_print_mrisc_cfg_with_jumps = ref false
let opt_print_mrisc_live_regs = ref false
let opt_print_allocated_mrisc_cfg = ref false
let opt_print_reg_alloc_info = ref false
let anon_cnt = ref 0

let anon_fun anon_arg =
  incr anon_cnt;
  match !anon_cnt with
  | 1 -> num_regs := int_of_string_opt anon_arg
  | 2 -> src := anon_arg
  | 3 -> out := anon_arg
  | _ -> ()

let speclist =
  [
    ( "num_regs",
      Arg.String anon_fun,
      "Available number of registers in the target architecture (at least 4)" );
    ("src", Arg.String anon_fun, "Source file to execute");
    ("out", Arg.String anon_fun, "Output file to write the compiled code to");
    ( "--check-undef-vars",
      Arg.Set opt_check_undef_vars,
      "Check for undefined variables in the input program and report an error \
       if any are found" );
    ( "--liveness-optimization",
      Arg.Set opt_liveness_optimization,
      "Enable optimization using static liveness analysis" );
    ( "--enable-constant-propagation",
      Arg.Set opt_constant_propagation,
      "Enable constant propagation" );
    ( "--print-tokens",
      Arg.Set opt_print_tokens,
      "Print the list of tokens returned by the scanner" );
    ( "--print-ast",
      Arg.Set opt_print_ast,
      "Print the parsed program bracketed according to the AST returned by the \
       parser" );
    ( "--print-mimp-cfg",
      Arg.Set opt_print_mimp_cfg,
      "Print the MiniImp CFG generated by the compiler" );
    ( "--print-mimp-defined-vars",
      Arg.Set opt_print_mimp_defined_vars,
      "Print the variables defined at each program point in the MiniImp CFG" );
    ( "--print-mrisc-cfg",
      Arg.Set opt_print_mrisc_cfg,
      "Print the MiniRISC CFG generated by the compiler" );
    ( "--print-mrisc-cfg-with-jumps",
      Arg.Set opt_print_mrisc_cfg_with_jumps,
      "Print the MiniRISC CFG generated by the compiler, including jump \
       instructions" );
    ( "--print-mrisc-live-regs",
      Arg.Set opt_print_mrisc_live_regs,
      "Print the in and out live registers at each program point" );
    ( "--print-allocated-mrisc-cfg",
      Arg.Set opt_print_allocated_mrisc_cfg,
      "Print the final MiniRISC CFG after register allocation" );
    ( "--print-reg-alloc-info",
      Arg.Set opt_print_reg_alloc_info,
      "Print the set of spilled registers and the register coloring after \
       register allocation" );
  ]

let usage_msg =
  "Usage: mimpc <num_regs> <src> <out> [--check-undef-vars] \
   [--liveness-optimization] [--enable-constant-propagation] [--print-tokens] \
   [--print-ast] [--print-mimp-cfg] [--print-mimp-defined-vars] \
   [--print-mrisc-cfg] [--print-mrisc-cfg-with-jumps] \
   [--print-mrisc-live-regs] [--print-allocated-mrisc-cfg] \
   [--print-reg-alloc-info]"

let print_tokens = List.iter (MiniImpLib.Utils.string_of_token >> print_endline)
let print_ast = MiniImpLib.Utils.string_of_ast >> print_endline
let print_mini_imp_cfg = MiniImpLib.Utils.string_of_cfg >> print_endline
let print_mini_risc_cfg = MiniRISCLib.Utils.string_of_cfg >> print_endline

let print_mini_risc_cfg_with_jumps =
  MiniRISCLib.Utils.string_of_cfg_with_jumps >> print_endline

let print_mrisc_live_regs mini_risc_cfg_with_jumps =
  let live_regs =
    MiniRISCLib.LiveRegistersAnalysis.live_regs_analysis
      mini_risc_cfg_with_jumps
  in
  MiniRISCLib.Utils.string_of_live_regs mini_risc_cfg_with_jumps live_regs
  |> print_endline

let print_reg_alloc_info spilled_regs reg_coloring =
  let spilled_regs_str =
    spilled_regs |> MiniRISCLib.RegUtils.RegSet.elements
    |> List.map MiniRISCLib.Utils.string_of_reg
    |> String.concat ", "
  in
  let reg_coloring_str =
    reg_coloring |> MiniRISCLib.RegUtils.RegMap.bindings
    |> List.map (fun (virtual_reg, physical_reg) ->
        Printf.sprintf "%s -> %s"
          (MiniRISCLib.Utils.string_of_reg virtual_reg)
          (MiniRISCLib.Utils.string_of_reg physical_reg))
    |> String.concat "\n"
  in
  Printf.printf "Spilled registers: %s\n" spilled_regs_str;
  Printf.printf "Register coloring:\n%s\n" reg_coloring_str

let () =
  Arg.parse speclist anon_fun usage_msg;
  let bad_num_regs =
    match !num_regs with
    | None -> true
    | Some num_regs when num_regs < 4 -> true
    | _ -> false
  in
  if !src = "" || !out = "" || bad_num_regs || !anon_cnt <> 3 then
    Arg.usage speclist usage_msg
  else
    try
      let num_regs = Option.get !num_regs in
      if !opt_print_tokens then !src |> MiniImpLib.scan |> print_tokens;
      let ast = MiniImpLib.parse !src in
      let ast =
        if !opt_constant_propagation then MiniImpLib.propagate_constants ast
        else ast
      in
      if !opt_print_ast then print_ast ast;
      let mimp_cfg = MiniImpLib.build_cfg ast in
      if !opt_print_mimp_cfg then print_mini_imp_cfg mimp_cfg;
      if !opt_check_undef_vars then
        MiniImpLib.run_defined_vars_analysis mimp_cfg;
      let mrisc_cfg = MiniRISCLib.translate_cfg mimp_cfg in
      if !opt_print_mrisc_cfg then print_mini_risc_cfg mrisc_cfg;
      let mrisc_cfg = MiniRISCLib.add_jump_instrs mrisc_cfg in
      if !opt_print_mrisc_cfg_with_jumps then
        print_mini_risc_cfg_with_jumps mrisc_cfg;
      if !opt_print_mrisc_live_regs then print_mrisc_live_regs mrisc_cfg;
      let mrisc_cfg, spilled_regs, reg_coloring =
        if !opt_liveness_optimization then
          MiniRISCLib.optimized_register_allocation num_regs mrisc_cfg
        else MiniRISCLib.unoptimized_register_allocation num_regs mrisc_cfg
      in
      if !opt_print_allocated_mrisc_cfg then
        print_mini_risc_cfg_with_jumps mrisc_cfg;
      if !opt_print_reg_alloc_info then
        print_reg_alloc_info spilled_regs reg_coloring;
      MiniRISCLib.compile !out mrisc_cfg;
      Printf.printf "%s Successfully compiled %s to %s\n" (color_text green "🗸")
        !src !out
    with
    | MiniImpLib.Lexer.LexicalError msg ->
        Printf.eprintf "%s %s\n" (color_text red "[Lexical error]") msg
    | MiniImpLib.Parser.Error -> prerr_endline (color_text red "[Syntax error]")
    | GenericCFG.CFGError msg ->
        Printf.eprintf "%s %s\n" (color_text red "[CFG error]") msg
    | MiniImpLib.DefinedVariablesAnalysis.UndefinedVariable msg ->
        Printf.eprintf "%s %s\n"
          (color_text red "[Undefined variable(s) error]")
          msg
    | MiniRISCLib.RegisterAllocation.RegisterAllocationError msg ->
        Printf.eprintf "%s %s\n"
          (color_text red "[Register allocation error]")
          msg
    | Failure msg -> Printf.eprintf "%s %s\n" (color_text red "[Error]") msg
